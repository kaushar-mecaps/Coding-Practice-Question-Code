
public class WideningCasting {
    public static void main(String[] args) {

        int a = 10;
        double d = a;
        char b = 'k';
        int c = b;

        System.out.println("Before type casting int = " +a);
        System.out.println("After Type Casting double = " + d);
        System.out.println("Before Type Casting Char = " +b);
        System.out.println("After Type Casting int = " +c);

    }
}
