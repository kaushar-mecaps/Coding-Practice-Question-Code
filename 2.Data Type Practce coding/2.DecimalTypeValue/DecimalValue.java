public class DecimalValue {
    public static void main(String[] args) {
        float temperature = 45.152f;
        double percentage = 95.2545;
        double petrolPrice = 96.564;

        System.out.println("----- Decimal Value ( 2 Decimal Place ) -----");
        System.out.printf("Temperature : %.2f\n", temperature);
        System.out.printf("Percentage : %.2f\n", percentage);
        System.out.printf("Petrol Price : %.2f\n",petrolPrice);
    }
}
