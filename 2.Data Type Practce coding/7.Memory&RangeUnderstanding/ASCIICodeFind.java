public class ASCIICodeFind {
    public static void main(String[] args) {
        // Local variable 
        char a = 'a';
        // print character
        System.out.println("Character = " + a);
        // Convert char into ASCII Number
        int asciiNumber = a;
        // Print ASCII Number
        System.out.println("ASCII Number of a = " + asciiNumber);
    }
}
