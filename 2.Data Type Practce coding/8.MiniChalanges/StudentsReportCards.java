public class StudentsReportCards {
    public static void main(String[] args) {
        // Local Variable String Type 
        String studentName = "Kaushar Ali";
        // Byte Type Variable
        byte age = 24;
        // int Type
        int rollNumber = 45;
        // float type
        float marks = 420f;
        // boolean Type
        boolean isPassed = true;

        // Print Variable 
        System.out.println("--------- Student Report Card ---------------");

        System.out.println("Student Name = " + studentName + "\n Age = " + age + 
                            "\n Roll Number = " + rollNumber + "\n Marks = " + marks + 
                            "\n Is Pass = " + isPassed);
        
        System.out.println("---------------------------------------------");
    }
}
