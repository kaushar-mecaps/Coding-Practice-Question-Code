public class DataTypeGuessGame {
    public static void main(String[] args) {
        // Actually int type variable
        var a = 10;
        // double type
        var b = 10.5;
        // Character type
        var c = 'A';
        // boolean type
        var d = true;

        // All Variable Print here
        System.out.println("int type = " + a);
        System.out.println("Double Type = " +b);
        System.out.println("Character Type = " + c);
        System.out.println("Boolean Type = " + d);
    }
}
