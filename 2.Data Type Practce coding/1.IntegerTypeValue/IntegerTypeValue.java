public class IntegerTypeValue {
    public static void main(String[] args) {

        byte age = 24;
        short bankBalance = 5000;
        int pinCode = 274304;
        long mobileNumber = 7408089771L;

        System.out.println("------Personal information------");
        System.out.println("My age = " + age);
        System.out.println("My Bank Balance = " + bankBalance);
        System.out.println("My Pin Code = " + pinCode);
        System.out.println("My Phone Number = " + mobileNumber);

    }
}